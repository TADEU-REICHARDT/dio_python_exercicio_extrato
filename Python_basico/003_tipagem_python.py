
'''
Python = Linguagem de programação de tipagem  =>dinamica forte

str -> string -> texto

Stings são textos que estão dentro de aspas
'''

# ASpas simples
print('Tadeu Reichardt')


# ASpas duplas
print("Tadeu Reichardt")


# Escape
print('Tadeu \"Reichardt') # vai aparecer as aspas


# r
print(r'Tadeu \"Reichardt') # vai aparecer a barra e aspas

# Truque para as aspas

print('Tadeu "Reichardt"') # vai aparecer as aspas de dentro, quando forem diferente das de fora



