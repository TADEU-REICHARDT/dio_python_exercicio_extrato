print(1, 23, 25,  26) # PRINT = imprimir

print(1, 34, 25,  26, sep=" @ ")  # SEP = separador (se deixar vazio ele apenas adiciona um espaço) 

print(1, 34, 25,  26, sep=" ")  # SEP = separador (se deixar vazio ele apenas adiciona um espaço) 

print(1, 34, 25,  26, sep="-", end='>>')  # end = Tira a quebra de linha ( não pula a linha  para o proximo print) e adiciona os caracteres

print(1, 34, 25,  26, sep="-", end='>>')  # end = adiciona oas caracteres no final do Print  